    function getFilteredIcons() {
      var list = [];

      if (currentFilter === 'all') {
        list = REGULAR_ICONS.map(function(n) { return { name: n, prefix: 'wf' }; })
          .concat(SOLID_ICONS.map(function(n) { return { name: n, prefix: 'wfs' }; }))
          .concat(LOGOS_ICONS.map(function(n) { return { name: n, prefix: 'wfl' }; }));
      } else if (currentFilter === 'wf') {
        list = REGULAR_ICONS.map(function(n) { return { name: n, prefix: 'wf' }; });
      } else if (currentFilter === 'wfs') {
        list = SOLID_ICONS.map(function(n) { return { name: n, prefix: 'wfs' }; });
      } else if (currentFilter === 'wfl') {
        list = LOGOS_ICONS.map(function(n) { return { name: n, prefix: 'wfl' }; });
      }

      if (currentSearch) {
        list = list.filter(function(icon) {
          return icon.name.indexOf(currentSearch) !== -1;
        });
      }

      return list;
    }

    function render() {
      var list = getFilteredIcons();
      var ul = document.getElementById('wficons-list');
      var countEl = document.getElementById('icon-count');

      if (!ul) return;

      ul.innerHTML = '';

      list.forEach(function(icon) {
        var li = document.createElement('li');
        var iconClass = icon.prefix + ' ' + icon.prefix + '-' + icon.name;
        var copyText = icon.prefix + '-' + icon.name;

        li.innerHTML =
          '<a href="#" data-copy="' + copyText + '">' +
          '<div><i class="' + iconClass + '"></i></div>' +
          '<p>' + copyText + '</p>' +
          '</a>';

        ul.appendChild(li);

        var a = li.querySelector('a');
        if (a) {
          a.addEventListener('click', function(e) {
            e.preventDefault();
            var copyName = this.getAttribute('data-copy') || '';

            try {
              navigator.clipboard.writeText(copyName).then(function() {
                a.classList.add('copied');
                var p = a.querySelector('p');

                if (p) {
                  var orig = p.textContent;
                  p.textContent = copyName + ' copiado!';

                  setTimeout(function() {
                    p.textContent = orig;
                    a.classList.remove('copied');
                  }, 1200);
                } else {
                  setTimeout(function() {
                    a.classList.remove('copied');
                  }, 1200);
                }
              });
            } catch (_) {}
          });
        }
      });

      if (countEl) {
        var totalText = list.length + ' de ';

        if (currentFilter === 'all') {
          totalText += (REGULAR_ICONS.length + SOLID_ICONS.length + LOGOS_ICONS.length) + ' ícones';
        } else if (currentFilter === 'wf') {
          totalText += REGULAR_ICONS.length + ' ícones';
        } else if (currentFilter === 'wfs') {
          totalText += SOLID_ICONS.length + ' ícones';
        } else if (currentFilter === 'wfl') {
          totalText += LOGOS_ICONS.length + ' ícones';
        }

        countEl.textContent = totalText;
      }
    }

