    if (document.readyState === 'loading') {
      document.addEventListener('DOMContentLoaded', init);
    } else {
      init();
    }

    try {
      document.addEventListener('wfajax:loaded', init);
      document.addEventListener('wfajax:complete', init);
    } catch (_) {}
  })();
